    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String Email = jTextField1.getText();
        String passWord = jPasswordField1.getText();
        pharmacy = new Pharmacy();
        
        AbstractFileReader reader = new AbstractFileReader();
        pharmacy.setCustomer(reader.getCustomers());
        pharmacy.setStaff(reader.getPeople());
        
        if (this.jRadioButton1.isSelected())
        {
         Customer c = new Customer();
         c = pharmacy.loginCustomer(Email, passWord);
        
         if(c==null)
         {
             Component frame = null;
             JOptionPane.showMessageDialog(frame,"Wrong Email or Password","Inane error",JOptionPane.ERROR_MESSAGE);
            
         }
         else {
             String name =  c.getFirstName();
            JOptionPane.showMessageDialog(null, "Welcome "+ name );
            new CustomerOperations().setVisible(true);
            this.setVisible(false);
            setCustomer(c);
            this.Ncustomer = c;
         }

        }
        else if(this.jRadioButton2.isSelected())
        {
           Staff newStaff = pharmacy.logInStaff(Email, passWord);
           
           
        if(newStaff == null)
         {
             Component frame = null;
             JOptionPane.showMessageDialog(frame,"Wrong Email or Password","Inane error",JOptionPane.ERROR_MESSAGE);
         }
        else{

            JOptionPane.showMessageDialog(null, "Welcome "+ newStaff.getFirstName() );
                if (newStaff.getPosition().equals("Administrative")) {
                    new AdminOperation ().setVisible(true);
                    this.setVisible(false);
            }
                else{
                     new DeliveryOperation().setVisible(true);
                     this.setVisible(false);
                    }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

