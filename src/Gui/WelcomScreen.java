    private void intilizePharmacy(){
        Pharmacy pharmacy = new Pharmacy();
        
        

       
        //Retrive Customers Data From files
        AbstractFileReader reader = new AbstractFileReader();
        pharmacy.setCustomer(reader.getCustomers());
        
        //Retive Staff Data from files
        AbstractFileReader Staffreader = new AbstractFileReader();
        pharmacy.setStaff(Staffreader.getPeople());
        
        //Retrive Products Data from files
        AbstractFileReader productReader = new AbstractFileReader();
        pharmacy.setProduct(productReader.getProduct());
        
        //Retrive Orders data from files
        AbstractFileReader OrderReader = new AbstractFileReader();
        pharmacy.seOrders(OrderReader.getAllOrders());
      }
